# ---------------------------------------------------------------------------
# 5. Set up src/app/layout.tsx with dark mode provider, Geist font, metadata
# ---------------------------------------------------------------------------
echo "⚙️  Setting up layout.tsx..."
mkdir -p src/app
cat > src/app/layout.tsx << 'LAYOUT_EOF'
